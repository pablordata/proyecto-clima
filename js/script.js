/**
 * Alterna entre modo claro y oscuro de la aplicaci√≥n
 * - Cambia el estado de isDarkMode
 * - A√±ade/remueve clase CSS 'dark-mode' del body
 * - Actualiza icono del bot√≥n (üåô para claro, ‚òÄÔ∏è para oscuro)
 * - Guarda preferencia en sessionStorage
 */
function toggleTheme() {
    // Cambiar estado del tema
    isDarkMode = !isDarkMode;
    const body = document.body;
    const themeIcon = document.getElementById('themeIcon');
    
    if (isDarkMode) {
        // Activar modo oscuro
        body.classList.add('dark-mode');
        themeIcon.textContent = '‚òÄÔ∏è'; // Icono de sol para indicar "cambiar a claro"
        sessionStorage.setItem('darkMode', 'true');
    } else {
        // Activar modo claro
        body.classList.remove('dark-mode');
        themeIcon.textContent = 'üåô'; // Icono de luna para indicar "cambiar a oscuro"
        sessionStorage.setItem('darkMode', 'false');
    }
}

/**
 * Carga el tema guardado al inicializar la aplicaci√≥n
 * Lee la preferencia desde sessionStorage y aplica el tema correspondiente
 * Se ejecuta autom√°ticamente al cargar la p√°gina
 */
function loadTheme() {
    const savedTheme = sessionStorage.getItem('darkMode');
    
    if (savedTheme === 'true') {
        // Cargar modo oscuro
        isDarkMode = true;
        document.body.classList.add('dark-mode');
        document.getElementById('themeIcon').textContent = '‚òÄÔ∏è';
    } else {
        // Cargar modo claro (por defecto)
        isDarkMode = false;
        document.body.classList.remove('dark-mode');
        document.getElementById('themeIcon').textContent = 'üåô';
    }
}

