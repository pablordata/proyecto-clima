/**
 * Convierte grados de direcci√≥n del viento en puntos cardinales
 * Divide los 360¬∞ en 8 secciones de 45¬∞ cada una
 * 
 * @param {number} degrees - Direcci√≥n del viento en grados (0-360)
 * @returns {string} Punto cardinal (N, NE, E, SE, S, SO, O, NO)
 */
function getWindDirection(degrees) {
    const directions = ['N', 'NE', 'E', 'SE', 'S', 'SO', 'O', 'NO'];
    // Calcular √≠ndice: dividir grados entre 45, redondear y aplicar m√≥dulo 8
    const index = Math.round(degrees / 45) % 8;
    return directions[index];
}

/**
 * Formatea fechas para el pron√≥stico de manera amigable
 * Convierte fechas ISO en texto legible en espa√±ol
 * 
 * @param {string} dateString - Fecha en formato ISO (YYYY-MM-DD)
 * @returns {string} Fecha formateada ("Hoy", "Ma√±ana", o "d√≠a, n√∫mero")
 */
function formatDate(dateString) {
    // Crear objeto Date agregando hora 00:00:00 para evitar problemas de zona horaria
    const date = new Date(dateString + 'T00:00:00');
    
    // Obtener fecha actual y de ma√±ana para comparaci√≥n
    const today = new Date();
    today.setHours(0,0,0,0); // Normalizar a medianoche
    
    const tomorrow = new Date(today);
    tomorrow.setDate(tomorrow.getDate() + 1);

    // Determinar etiqueta apropiada
    if (date.toDateString() === today.toDateString()) {
        return 'Hoy';
    } else if (date.toDateString() === tomorrow.toDateString()) {
        return 'Ma√±ana';
    } else {
        // Formato: "lun 15" (d√≠a de semana abreviado + n√∫mero)
        return date.toLocaleDateString('es-ES', { 
            weekday: 'short', 
            day: 'numeric'
        }).replace('.', ''); // Remover punto del d√≠a de la semana
    }
}

